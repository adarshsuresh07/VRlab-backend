const mongoose = require("mongoose");

const teacherSchema = new mongoose.Schema({
  fullname: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
  verified: {
    type: Boolean,
    default: false,
  },
  experiments_started: {
    type: [{ id: mongoose.ObjectId }],
  },
  created_at: {
    type: Date,
    default: Date.now,
  },
  updated_at: {
    type: Date,
    default: Date.now,
  },
});

const teacher = mongoose.model("teacher", teacherSchema);
module.exports = teacher;
